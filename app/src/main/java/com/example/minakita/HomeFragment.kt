            param2 = it.getString(ARG_PARAM2)
        }
    }

    override fun onCreateView(inflater: LayoutInflater, container: ViewGroup?, savedInstanceState: Bundle?): View? {
        // Inflate the layout for this fragment
        val view = inflater.inflate(R.layout.fragment_home, container, false)

        //set viewpager2
        view.berbagi.adapter = BerbagiAdapter(listData())
        view.berbagi.clipToPadding = false
        view.berbagi.clipChildren = false
        view.berbagi.offscreenPageLimit = 3
        view.berbagi.getChildAt(0).overScrollMode = RecyclerView.OVER_SCROLL_NEVER
        val compositePageTransformer = CompositePageTransformer()
        compositePageTransformer.addTransformer(MarginPageTransformer(15))
        view.berbagi.setPageTransformer(compositePageTransformer)

